public class Main {
    public static void main(String[] args) {
        int varInt = 1;
        byte varByte = 1;
        short varShort = 1;
        long varLong = 1;
        float varFloat = 1f;
        double varDouble = 1.0;

        System.out.println("Значение переменной varInt c типом int равно " + varInt);
        System.out.println("Значение переменной byte c типом int равно " + varByte);
        System.out.println("Значение переменной short c типом int равно " + varShort);
        System.out.println("Значение переменной long c типом int равно " + varLong);
        System.out.println("Значение переменной float c типом int равно " + varFloat);
        System.out.println("Значение переменной double c типом int равно " + varDouble);
    }
}